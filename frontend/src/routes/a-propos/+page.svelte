<h1 class="heading-xl">À propos de Nplex</h1>

<h2 class="heading-lg">Un projet mené par la chaire UNESCO</h2>
<p>
	Les activités de la Chaire UNESCO en paysage urbain de l’Université de Montréal (CUPUM) tiennent
	pour objectif de répondre à des enjeux concrets de préservation, de mise en valeur et de
	développement des paysages urbains pour les gouvernements locaux. À travers les visions et projets
	qu’elle déploie, la CUPUM vise contribuer à l’éducation, à la sensibilisation ainsi qu’à
	l’accompagnement des élus et des experts municipaux, des gouvernements, des citoyens pour le
	devenir de leurs paysages et de la qualité des cadres de vie urbains. En accord avec ce rôle et en
	réponse directe à l’appel de projet Développer ensemble une plus forte culture du design lancé par
	le Bureau du design de la Ville de Montréal, la CUPUM, sous la direction de son titulaire Shin
	Koseki (Dr.Sc., professeur adjoint à l’École d’urbanisme et d’architecture de paysage de
	l’Université de Montréal) entreprend la conception et le développement d’une application web
	dédiée à la valorisation de la qualité et de l’exemplarité au niveau des petits projets
	d’aménagement sur le territoire de la Ville.
</p>
<h2>Une problématique soulignée par la Ville de Montréal et son Bureau du design</h2>
<p>
	La crise sanitaire de la COVID-19 a bouleversé nos modes d'habiter et de travailler. En réponse,
	les grandes municipalités du Québec comme la ville de Montréal cherchent à promouvoir des
	initiatives qui proposent de nouvelles configurations des espaces de vie et de travail. Alors que
	le télétravail depuis la maison semble s’imposer comme réalité durable, nos modes d’habiter ont
	aussi évolué sur l’espace public par la mise en place rapide de diverses typologies d’aménagements
	temporaires répondants aux nouveaux besoins d’espaces de socialisation extérieurs. Dans ce
	contexte, la ville connait un nombre important de demandes de permis initiées par des citoyens
	pour des projets d’échelle modeste dans le domaine résidentiel et public. Afin de répondre à la
	pression exercée par le secteur de la construction sur ses ressources internes, tout en maintenant
	sa mission de promouvoir des pratiques de constructions durables et en harmonie avec le contexte
	architectural et urbanistique de la ville, la CUPUM doit formuler un projet répondant aux besoins
	de cinq publics cibles prioritaires&nbsp;:
</p>
<ul>
	<li>les citoyens demandeurs de permis de construction</li>
	<li>les citoyens impliqués dans des processus participatifs</li>
	<li>les citoyens membres de comités consultatifs d’urbanisme (CCU)</li>
	<li>les élus municipaux</li>
	<li>les professionnels de l’aménagement qui encadre des processus consultatifs d’aménagement</li>
</ul>
<p>
	Devant à la fois faire la promotion des professionnels du design, et présenter les meilleures
	solutions en termes de conception architecturale et d’aménagement de l’espace public, la
	proposition doit être accessible par tous pour constituer un apport pertinent tant dans la
	coordination entre les collaborateurs de la Ville que dans les échanges entre ceux-ci et les
	citoyens.
</p>
<h2>Un projet-réponse: l'application Nplex</h2>
<p>
	En collaboration avec le Bureau du design de la Ville de Montréal, la CUPUM entend développer une
	application web, intitulée nplex, qui constituera un portail de documentation, de diffusion, et
	d'exploration de petits projets d’architecture et d’aménagement locaux ayant produit, à partir de
	moyens modestes, des résultats jugés exemplaires sur le plan du design, de la réhabilitation
	d’espace de vie et de travail, de l’intégration au contexte, de la démarche de concours, du
	processus de budget participatif, des demandes de permis, de la négociation de dérogations, et
	plus. La plateforme nplex présente une approche alternative aux sites spécialisés dans les grands
	projets d’architecture en misant sur l’exemplarité de projets d’extension, de restauration
	(revêtements de façades, portes et fenêtres, etc.) de surélévation, d’aménagement temporaire de
	rues et ruelles, de places et de parcs par des initiatives publiques ou citoyennes. Les projets y
	seront documentés sous forme de fiches composées de champs d’information communs portant entre
	autres sur le type de projet, la fourchette du coût de construction, les matériaux et leur
	provenance, le processus d’obtention de permis, des intervenants professionnels, et les aides et
	accompagnements possibles par la ville et la province. Les utilisateurs pourront facilement
	découvrir des exemples de comparables à travers les différents arrondissements et mettre en
	contraste plusieurs projets par une carte multifonction, une liste de fiches, et des
	fonctionnalités de recherche avancées mettant à profit cette organisation commune des données. En
	tant que plateforme publique, nplex mise sur l’interactivité et la représentation simplifiée et
	accessible des données de contexte à l’attention des citoyens et des professionnels. En plus des
	fiches de projets, les utilisateurs y trouveront entre autres des informations géographiques
	d'intérêt comme les contraintes contextuelles des lieux à considérer telles que des éléments
	patrimoniaux ou culturels d’importance, des plans d'implantation, des désignations particulières
	de secteurs, le zonage, les aires et unités de paysage, les dérogations, etc. Ce projet
	d’innovation sociale vise à remplir les trois objectifs suivants&nbsp;:
</p>
<ul>
	<li>
		Co-construire une plateforme numérique de valorisation des petits projets exemplaires
		d’architecture et d’aménagement de l’espace public en matière de design et de durabilité
	</li>
	<li>
		Rendre accessibles des données probantes sur la réalisation de petits projets exemplaires locaux
		aux différents publics cibles identifiés par la ville de Montréal
	</li>
	<li>
		Faciliter la coordination des collaborateurs à l’interne de la ville impliqués dans processus
		faisant appel aux publics cibles identifiés et la mise en place d’un dialogue productif et
		efficace entre ceux-ci et les citoyens
	</li>
</ul>
<h2>Contenu de la plateforme</h2>
<p>
	Il est prévu que l'application web mettra en place les systèmes nécessaires pour présenter un
	corpus de fiches complémentaires et référées de manières croisées&nbsp;:
</p>
<ul>
	<li>des fiches de projet</li>
	<li>
		des fiches d'entreprise, d'organisation, ou d'institution qui interviennent à une étape ou une
		autre dans la démarche des projets documentés
	</li>
</ul>

